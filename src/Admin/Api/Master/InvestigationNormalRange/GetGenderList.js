async function GetGenderList() {
    let url = window.AppbaseUrl+"/api/PatientRegistration/GetAllGender";
  let head = { "Content-Type": "application/JSON", accept : '*/*' };
  
  
  let response = await fetch(url, {
    headers: head,
    method : 'GET'
  })
    .then((res) => res.json())
    .then();
  
  return response;
  }
  export default GetGenderList;